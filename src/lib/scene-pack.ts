/**
 * 场景包：BD 商务拓展。
 * 结构与雷达/生产工程的本体对齐——
 *   环节(6) → 专员(每环节 1 位，用户交互的人格面)
 *           → 任务(每专员 3-5 个，一等公民)：每个任务对应雷达的一张需求卡，
 *             有独立方法论提示词(taskPrompt)、独立 webSearch 开关、独立 skillRef 换装点。
 * 生产工程交付 skill 包时按【任务】换装，不是按专员。
 * 点任务 → 用该任务的 taskPrompt；自由聊天 → 专员的轻量 basePrompt。
 */

export interface AgentTask {
  key: string;
  label: string;
  /** 点击后代替用户发出的消息（含"粘贴在这里"的会先填入输入框） */
  prompt: string;
  /** 该任务的专属方法论提示词（对应雷达一张需求卡的交付要求） */
  taskPrompt: string;
  /** 该任务是否需要联网搜索（默认否） */
  webSearch?: boolean;
  /** 雷达需求卡对照（备注用） */
  cardRef?: string;
  /** 生产工程交付对应 skill 包后在此换装 */
  skillRef?: string | null;
}

export interface StageAgent {
  key: string;
  name: string;
  emoji: string;
  /** 工作台顶部对用户的一句话自我介绍 */
  intro: string;
  /** 自由聊天时的轻量人格底座：角色、职责边界、知道自己有哪些任务并主动推荐 */
  basePrompt: string;
}

export interface SceneStage {
  key: string;
  name: string;
  description: string;
  coming?: boolean;
  agent?: StageAgent;
  tasks?: AgentTask[];
}

